    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        deleteBtn.setEnabled(true);
        editBtn.setEnabled(true);

        int row = jTable1.rowAtPoint(evt.getPoint());
        int col = jTable1.columnAtPoint(evt.getPoint());

        selectedPersonel = new Personel();

        selectedPersonel.setPersonelID(jTable1.getValueAt(row, 0).toString());
        selectedPersonel.setName(jTable1.getValueAt(row, 1).toString());
        selectedPersonel.setFirstname(jTable1.getValueAt(row, 2).toString());
        selectedPersonel.setStatut(jTable1.getValueAt(row, 3).toString());

    }//GEN-LAST:event_jTable1MouseClicked

