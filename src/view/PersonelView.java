    public final void setUpJTableModel() {
        defaultTableModel = new DefaultTableModel();
        jTable1.setModel(defaultTableModel);
        defaultTableModel.addColumn("PERSONEL ID");
        defaultTableModel.addColumn("NOM");
        defaultTableModel.addColumn("PRENOM");
        defaultTableModel.addColumn("STATUT");
        refreshTable();
    }

    private void refreshTable() {

        deleteBtn.setEnabled(false);
        editBtn.setEnabled(false);

        defaultTableModel.getDataVector().clear();

        ArrayList<Personel> personels = refreshRecord();

        personels.stream().forEach((currentPersonel) -> {
            defaultTableModel.addRow(new Object[]{currentPersonel.getPersonelID(), currentPersonel.getName(), currentPersonel.getFirstname(), currentPersonel.getStatut()});
        });
        defaultTableModel.fireTableDataChanged();
    }

    private ArrayList<Personel> refreshRecord() {
        return databaseManager.getAllPersonels();
    }

    @Override
    public void savePersonel(Personel personel) {
        databaseManager.addPersonel(personel);
        refreshTable();
    }

    @Override
    public void editPersonel(Personel personel) {
        databaseManager.updatePersonel(personel);
        refreshTable();
    }
