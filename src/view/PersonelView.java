    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int option = JOptionPane.showConfirmDialog(this, "Are you sure to delete this Personel Record?", "Confirm Personel Deletion", JOptionPane.ERROR_MESSAGE);
        if (option == 0) {
            databaseManager.removeEmprunt(selectedPersonel.getPersonelID());
            refreshTable();
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        String keyword = searchField.getText();

        deleteBtn.setEnabled(false);
        editBtn.setEnabled(false);

        defaultTableModel.getDataVector().clear();

        ArrayList<Personel> personels = databaseManager.searchPersonel(this, keyword);
        
        personels.stream().forEach((currentPersonel) -> {
            defaultTableModel.addRow(new Object[]{currentPersonel.getPersonelID(), currentPersonel.getName(), currentPersonel.getFirstname(), currentPersonel.getStatut()});
        });
        defaultTableModel.fireTableDataChanged();

        if (personels.isEmpty()) {
            refreshTable();
        }
    }//GEN-LAST:event_searchBtnActionPerformed

