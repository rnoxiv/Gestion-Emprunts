    public final void setUpJTableModel() {
        defaultTableModel = new DefaultTableModel();
        jTable1.setModel(defaultTableModel);
        defaultTableModel.addColumn("EMPRUNT ID");
        defaultTableModel.addColumn("DATE D'EMPRUNT");
        defaultTableModel.addColumn("EMPRUNTEUR");
        defaultTableModel.addColumn("MATERIEL");
        defaultTableModel.addColumn("QUANTITE");
        defaultTableModel.addColumn("STATUT");
        defaultTableModel.addColumn("DATE DE RETOUR");
        refreshTable();
    }

    private void refreshTable() {

        deleteBtn.setEnabled(false);
        editBtn.setEnabled(false);

        defaultTableModel.getDataVector().clear();

        ArrayList<Emprunt> emprunts = refreshRecord();

        emprunts.stream().forEach((currentEmprunt) -> {
            defaultTableModel.addRow(new Object[]{currentEmprunt.getEmpruntId(), currentEmprunt.getEmpruntDate(), databaseManager.getPersonelName(currentEmprunt.getEmpruntPersonelID()), databaseManager.getDesignation(currentEmprunt.getEmpruntMaterielID()), currentEmprunt.getEmpruntQuantite(), currentEmprunt.getEmpruntStatut(), currentEmprunt.getEmpruntDateRetour()});
        });
        defaultTableModel.fireTableDataChanged();

    }

    private ArrayList<Emprunt> refreshRecord() {
        return databaseManager.getAllEmprunts();
    }

    @Override
    public void saveEmprunt(Emprunt emprunt) {
        databaseManager.addEmprunt(emprunt);
        refreshTable();
    }

    @Override
    public void editEmprunt(Emprunt emprunt) {
        databaseManager.updateEmprunt(emprunt);
        refreshTable();
    }
