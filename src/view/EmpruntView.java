    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        deleteBtn.setEnabled(true);
        editBtn.setEnabled(true);

        int row = jTable1.rowAtPoint(evt.getPoint());
        int col = jTable1.columnAtPoint(evt.getPoint());

        selectedEmprunt = new Emprunt();

        selectedEmprunt.setEmpruntId(jTable1.getValueAt(row, 0).toString());
        selectedEmprunt.setEmpruntDate(jTable1.getValueAt(row, 1).toString());
        selectedEmprunt.setEmpruntPersonel(jTable1.getValueAt(row, 2).toString());
        selectedEmprunt.setEmpruntMateriel(jTable1.getValueAt(row, 3).toString());
        selectedEmprunt.setEmpruntQuantite(jTable1.getValueAt(row, 4).toString());
        selectedEmprunt.setEmpruntStatut(jTable1.getValueAt(row, 5).toString());
        selectedEmprunt.setEmpruntMaterielID(databaseManager.getEmpruntMaterielID(selectedEmprunt.getEmpruntId()));
        String[] splited = selectedEmprunt.getEmpruntPersonel().split("\\s+");

        if (splited.length >= 3) {
            String[] splited2 = new String[2];
            for (int i = 0; i < splited.length - 1; i++) {
                splited2[0] += splited[i];
            }
            splited2[1] = splited[splited.length - 1];
            selectedEmprunt.setEmpruntPersonelID(databaseManager.getPeronelID(splited2));
        } else {
            selectedEmprunt.setEmpruntPersonelID(databaseManager.getPeronelID(splited));
        }
        if ("Rendu".equals(selectedEmprunt.getEmpruntStatut())) {
            selectedEmprunt.setEmpruntDateRetour(jTable1.getValueAt(row, 6).toString());
        }

    }//GEN-LAST:event_jTable1MouseClicked

