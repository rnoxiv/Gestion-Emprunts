package model;

public class Materiel {
    
    private String materielID, groupe, sousGroupe, designation, etat, dateAchat, dateRemplacement, dispo, emprunteur, datePret;

    public String getMaterielID() {
        return this.materielID;
    }

    public void setMaterielID(String materielID) {
        this.materielID = materielID;
    }

    public String getGroupe() {
        return this.groupe;
    }
    
    public void setGroupe(String groupe) {
        this.groupe = groupe;
    }

    public String getSousGroupe() {
        return this.sousGroupe;
    }

    public void setSousGroupe(String sousGroupe) {
        this.sousGroupe = sousGroupe;
    }

    public String getDesignation() {
        return this.designation;
    }

    public void setDesignation(String designation) {
        this.designation = designation;
    }

    public String getEtat() {
        return this.etat;
    }

    public void setEtat(String etat) {
        this.etat = etat;
    }

    public String getDateAchat() {
        return this.dateAchat;
    }

    public void setDateAchat(String dateAchat) {
        this.dateAchat = dateAchat;
    }

    public String getDateRemplacement() {
        return this.dateRemplacement;
    }

    public void setDateRemplacement(String dateRemplacement) {
        this.dateRemplacement = dateRemplacement;
    }

    public String getDispo() {
        return this.dispo;
    }

    public void setDispo(String dispo) {
        this.dispo = dispo;
    }

    public String getEmprunteur() {
        return this.emprunteur;
    }

    public void setEmprunteur(String emprunteur) {
        this.emprunteur = emprunteur;
    }

    public String getDatePret() {
        return this.datePret;
    }

    public void setDatePret(String datePret) {
        this.datePret = datePret;
    }
    
    
    
}
